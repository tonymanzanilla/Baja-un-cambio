function progressIndexByStepFromSteps(steps, path) {
  return Object.fromEntries(
    steps.map((routeStep, index) => [
      routeStep.id,
      index === steps.length - 1 ? path.length : Math.max(1, Math.round((index / (steps.length - 1)) * (path.length - 1)) + 1),
    ])
  );
}

function registerCircuit({ id, title, subtitle, routeSummary, routeSteps, mapRoutePoints }) {
  window.DRIVING_CIRCUITS = window.DRIVING_CIRCUITS ?? [];
  window.DRIVING_CIRCUITS.push({
    id,
    title,
    subtitle,
    heroTitle: `${title}, listo para calibrar`,
    routeSummary,
    routeSteps,
    contextualMessages: createContextualMessages(id),
    mapContextRules: createMapRules(id),
    mapRoutePoints,
    progressIndexByStep: progressIndexByStepFromSteps(routeSteps, mapRoutePoints),
    mapCenter: { lat: -34.59075, lng: -58.39415 },
    mapZoom: 16,
  });
}

